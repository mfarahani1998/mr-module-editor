def safe_text_include(
    entry: Entry,
    ctx: RuleContext,
    zip_root: str,
    reason: str,
    file_kind: str,
    matched_rule: str = "",
    allow_binary_for_explicit_include: bool = False,
) -> Decision:
    if file_too_large(entry, ctx):
        mb = (entry.size_bytes or 0) / (1024 * 1024)
        return skip_decision(
            entry,
            f"Excluded: file is {mb:.2f} MiB, above --max-file-mb ({ctx.max_file_bytes / (1024 * 1024):.2f} MiB).",
            "large_file",
            matched_rule,
        )

    is_text = is_probably_text_file(entry.path)
    if not is_text and not (allow_binary_for_explicit_include and ctx.allow_binary):
        return skip_decision(
            entry,
            "Excluded: file appears to be binary or non-text.",
            "binary_or_non_text",
            matched_rule,
        )

    if (
        is_text
        and not ctx.allow_potential_secrets
        and has_potential_secret_content(entry.path)
    ):
        return skip_decision(
            entry,
            "Excluded: text content matched a high-confidence potential-secret pattern. Use --allow-potential-secrets only after reviewing.",
            "potential_secret_content",
            matched_rule,
        )

    return include_decision(entry, reason, file_kind, zip_root, matched_rule)


def hard_safety_exclusion(entry: Entry, ctx: RuleContext) -> Optional[Decision]:
    rel = entry.rel_path

    if entry.is_symlink:
        return skip_decision(
            entry,
            "Excluded: symlink not followed or included, to avoid bundling files outside the Unity project.",
            "symlink",
        )

    # Avoid including the bundle itself if the caller writes it inside the project.
    try:
        if ctx.output_zip.resolve() == entry.path.resolve():
            return skip_decision(
                entry, "Excluded: output zip file itself.", "output_artifact"
            )
    except OSError:
        pass

    explicit_exclude = match_any(ctx.exclude_patterns, rel)
    if explicit_exclude:
        return skip_decision(
            entry,
            f"Excluded: matched --exclude-pattern / exclude file pattern '{explicit_exclude}'.",
            "explicit_exclude",
            explicit_exclude,
        )

    if not ctx.allow_sensitive:
        sensitive_match = match_any(ctx.sensitive_patterns, rel)
        if sensitive_match:
            return skip_decision(
                entry,
                f"Excluded: path/name matched sensitive-file pattern '{sensitive_match}'. Use --allow-sensitive only after reviewing.",
                "sensitive_path",
                sensitive_match,
            )

    return None


def classify_non_meta(entry: Entry, ctx: RuleContext, zip_root: str) -> Decision:
    rel = entry.rel_path
    ext = suffix_lower(rel)
    filename = Path(rel).name

    hard = hard_safety_exclusion(entry, ctx)
    if hard:
        return hard

    explicit_include = match_any(ctx.include_patterns, rel)

    gen_dir, gen_reason = generated_directory_reason(rel)
    if gen_dir and not explicit_include:
        return skip_decision(
            entry,
            f"Excluded: {gen_reason}",
            "generated_or_user_local_directory",
            gen_dir,
        )

    gen_file = generated_file_pattern(rel)
    if gen_file and not explicit_include:
        return skip_decision(
            entry,
            f"Excluded: generated/tool artifact matching '{gen_file}'.",
            "generated_file",
            gen_file,
        )

    if explicit_include:
        return safe_text_include(
            entry,
            ctx,
            zip_root,
            f"Included: matched explicit include pattern '{explicit_include}'.",
            "explicit_include",
            explicit_include,
            allow_binary_for_explicit_include=True,
        )

    vendor_match = vendor_or_sample_pattern(rel, ctx)
    if vendor_match:
        return skip_decision(
            entry,
            f"Excluded: matched likely third-party/vendor/sample asset pattern '{vendor_match}'. Use --include-vendor-assets or a targeted --include-pattern if the agent needs it.",
            "vendor_or_sample_asset",
            vendor_match,
        )

    if ext in BINARY_OR_LARGE_ASSET_EXTENSIONS:
        return skip_decision(
            entry,
            f"Excluded: '{ext}' is treated as a binary/large asset type, not coding-agent source.",
            "binary_or_large_asset",
        )

    # Root-level documentation/config worth showing to the agent.
    if "/" not in rel and filename in ROOT_TEXT_FILENAMES:
        return safe_text_include(
            entry,
            ctx,
            zip_root,
            "Included: root documentation or repository config.",
            "root_text_file",
        )

    # Project-local docs.
    if first_component(rel) in DOC_ROOTS and ext in (
        SOURCE_EXTENSIONS | TEXT_CONFIG_EXTENSIONS
    ):
        return safe_text_include(
            entry,
            ctx,
            zip_root,
            "Included: project documentation/source-like text under docs.",
            "documentation",
        )

    # Unity ProjectSettings are text in most modern Unity projects and useful for agent context.
    if is_under_root(rel, "ProjectSettings"):
        if ext in BINARY_OR_LARGE_ASSET_EXTENSIONS:
            return skip_decision(
                entry,
                f"Excluded: ProjectSettings file uses binary/large extension '{ext}'.",
                "binary_project_setting",
            )
        return safe_text_include(
            entry,
            ctx,
            zip_root,
            "Included: Unity ProjectSettings text/configuration file.",
            "project_settings",
        )

    # UPM package manifests and embedded package source/config.
    if is_under_root(rel, "Packages"):
        if filename in {"manifest.json", "packages-lock.json", "package.json"}:
            return safe_text_include(
                entry,
                ctx,
                zip_root,
                "Included: Unity Package Manager/package manifest.",
                "package_manifest",
            )
        if ext in SOURCE_EXTENSIONS:
            return safe_text_include(
                entry,
                ctx,
                zip_root,
                "Included: source-like file inside Packages.",
                "package_source",
            )
        if ext in TEXT_CONFIG_EXTENSIONS:
            return safe_text_include(
                entry,
                ctx,
                zip_root,
                "Included: text config/docs inside Packages.",
                "package_text_config",
            )
        if ext in UNITY_EDITOR_AUTHORED_EXTENSIONS:
            if ctx.include_editor_assets:
                return safe_text_include(
                    entry,
                    ctx,
                    zip_root,
                    "Included: Unity editor-authored package asset because --include-editor-assets was used.",
                    "unity_editor_authored_asset",
                )
            return skip_decision(
                entry,
                "Excluded: Unity editor-authored package asset; use --include-editor-assets if the agent needs this context.",
                "unity_editor_authored_asset",
            )
        return skip_decision(
            entry,
            "Excluded: Packages file type is not recognized as small source/config text.",
            "unrecognized_package_file",
        )

    # The main Assets tree: include hand-editable code/text, skip Unity editor-authored assets unless requested.
    if is_under_root(rel, "Assets"):
        if ext in SOURCE_EXTENSIONS:
            return safe_text_include(
                entry,
                ctx,
                zip_root,
                "Included: source-like file under Assets.",
                "asset_source",
            )
        if ext in TEXT_CONFIG_EXTENSIONS:
            return safe_text_include(
                entry,
                ctx,
                zip_root,
                "Included: text config/docs/data under Assets.",
                "asset_text_config",
            )
        if ext in UNITY_EDITOR_AUTHORED_EXTENSIONS:
            if ctx.include_editor_assets:
                return safe_text_include(
                    entry,
                    ctx,
                    zip_root,
                    "Included: Unity editor-authored asset because --include-editor-assets was used.",
                    "unity_editor_authored_asset",
                )
            return skip_decision(
                entry,
                "Excluded: Unity editor-authored asset normally edited in the Unity Editor; use --include-editor-assets for scene/prefab/material context.",
                "unity_editor_authored_asset",
            )
        if ctx.include_unknown_text:
            return safe_text_include(
                entry,
                ctx,
                zip_root,
                "Included: unknown small text file because --include-unknown-text was used.",
                "unknown_text",
            )
        return skip_decision(
            entry,
            "Excluded: Assets file type is not recognized as hand-editable code/config and is not included by default.",
            "unrecognized_asset_file",
        )

    # Some projects keep standalone package.json or tool config at the project root.
    if "/" not in rel and ext in TEXT_CONFIG_EXTENSIONS:
        return safe_text_include(
            entry,
            ctx,
            zip_root,
            "Included: root-level text configuration/documentation file.",
            "root_text_config",
        )

    return skip_decision(
        entry,
        "Excluded: outside default Unity source roots (Assets, Packages, ProjectSettings, project docs/root configs).",
        "outside_default_roots",
    )


def classify_meta(
    entry: Entry,
    ctx: RuleContext,
    zip_root: str,
    included_files: set[str],
    included_dirs: set[str],
) -> Decision:
    rel = entry.rel_path

    hard = hard_safety_exclusion(entry, ctx)
    if hard:
        return hard

    explicit_include = match_any(ctx.include_patterns, rel)
    gen_dir, gen_reason = generated_directory_reason(rel)
    if gen_dir and not explicit_include:
        return skip_decision(
            entry,
            f"Excluded: {gen_reason}",
            "generated_or_user_local_directory",
            gen_dir,
        )

    if explicit_include:
        return safe_text_include(
            entry,
            ctx,
            zip_root,
            f"Included: .meta matched explicit include pattern '{explicit_include}'.",
            "meta",
            explicit_include,
        )

    paired_rel = rel[:-5]  # strip trailing .meta
    paired_is_included_file = paired_rel in included_files
    paired_is_included_dir = paired_rel in included_dirs

    if paired_is_included_file:
        return safe_text_include(
            entry,
            ctx,
            zip_root,
            "Included: Unity .meta companion for an included asset, preserving GUID/import settings.",
            "meta",
        )
    if paired_is_included_dir:
        return safe_text_include(
            entry,
            ctx,
            zip_root,
            "Included: Unity .meta companion for a folder containing included assets.",
            "folder_meta",
        )

    vendor_match = vendor_or_sample_pattern(rel, ctx) or vendor_or_sample_pattern(
        paired_rel, ctx
    )
    if vendor_match:
        return skip_decision(
            entry,
            f"Excluded: .meta belongs to a likely third-party/vendor/sample asset pattern '{vendor_match}'. Use --include-vendor-assets or a targeted --include-pattern if needed.",
            "vendor_or_sample_meta",
            vendor_match,
        )

    if ctx.include_all_meta and (
        is_under_root(rel, "Assets")
        or is_under_root(rel, "Packages")
        or rel in {"Assets.meta", "Packages.meta"}
    ):
        return safe_text_include(
            entry,
            ctx,
            zip_root,
            "Included: --include-all-meta requested all safe Unity .meta files.",
            "meta",
        )

    return skip_decision(
        entry,
        "Excluded: Unity .meta file whose paired asset/folder is not included in this source bundle.",
        "unpaired_or_excluded_meta",
    )


def iter_project_entries(
    project_dir: Path, prune_generated_dirs: bool
) -> Iterable[Entry]:
    """Yield file entries. When prune_generated_dirs is true, emitted directory
    marker rows document the pruning, but files inside those dirs are not listed.
    """
    project_dir = project_dir.resolve()
    for root, dirs, files in os.walk(project_dir, topdown=True, followlinks=False):
        root_path = Path(root)

        if prune_generated_dirs:
            keep_dirs: list[str] = []
            for d in dirs:
                dir_path = root_path / d
                rel = rel_posix(dir_path, project_dir)
                gen_dir, gen_reason = generated_directory_reason(rel)
                try:
                    st = dir_path.lstat()
                    mtime = mtime_to_iso(st.st_mtime)
                except OSError:
                    st = None
                    mtime = ""
                if gen_dir:
                    yield Entry(
                        path=dir_path,
                        rel_path=rel + "/",
                        entry_type="directory",
                        size_bytes=None,
                        mtime_utc=mtime,
                        is_symlink=dir_path.is_symlink(),
                    )
                    continue
                keep_dirs.append(d)
            dirs[:] = keep_dirs

        for name in files:
            path = root_path / name
            try:
                st = path.lstat()
                size = st.st_size
                mtime = mtime_to_iso(st.st_mtime)
                is_link = path.is_symlink()
            except OSError:
                size = None
                mtime = ""
                is_link = False
            yield Entry(
                path=path,
                rel_path=rel_posix(path, project_dir),
                entry_type="file",
                size_bytes=size,
                mtime_utc=mtime,
                is_symlink=is_link,
            )


def resolve_repo_root(script_path: Path, explicit_repo_root: Optional[Path]) -> Path:
    if explicit_repo_root:
        return explicit_repo_root.resolve()
    # Expected location: repo-root/tools/build_unity_bundle.py
    if script_path.parent.name == "tools":
        return script_path.parent.parent.resolve()
    return Path.cwd().resolve()


def resolve_project_dir(repo_root: Path, project_arg: str) -> Path:
    project_path = Path(project_arg)
    candidates: list[Path] = []

    if project_path.is_absolute():
        candidates.append(project_path)
    else:
        candidates.append(repo_root.parent / project_path)
        candidates.append(repo_root / project_path)
        candidates.append(Path.cwd() / project_path)

    for candidate in candidates:
        if candidate.exists() and candidate.is_dir():
            return candidate.resolve()

    tried = "\n  ".join(str(c) for c in candidates)
    raise SystemExit(
        f"Could not find project directory for '{project_arg}'. Tried:\n  {tried}"
    )


def validate_unity_project(project_dir: Path, allow_non_unity: bool) -> None:
    assets = project_dir / "Assets"
    project_settings = project_dir / "ProjectSettings"
    packages = project_dir / "Packages"
    if assets.is_dir() and project_settings.is_dir():
        return
    if allow_non_unity:
        return
    raise SystemExit(
        f"'{project_dir}' does not look like a Unity project: expected at least Assets/ and ProjectSettings/. "
        "Use --allow-non-unity only if this is intentional."
    )


def build_included_dir_set(included_files: set[str]) -> set[str]:
    dirs: set[str] = set()
    for rel in included_files:
        parts = rel.split("/")[:-1]
        acc: list[str] = []
        for part in parts:
            acc.append(part)
            dirs.add("/".join(acc))
    return dirs


