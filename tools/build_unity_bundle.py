def extract_editor_build_scenes(project_dir: Path) -> list[dict[str, object]]:
    """Extract scene paths from ProjectSettings/EditorBuildSettings.asset.

    This is intentionally a light-weight parser for UnityYAML. It avoids a YAML
    dependency and only looks for the m_Scenes entries that Unity writes as
    enabled/path/guid records.
    """
    path = project_dir / "ProjectSettings" / "EditorBuildSettings.asset"
    if not path.is_file():
        return []

    scenes: list[dict[str, object]] = []
    current: Optional[dict[str, object]] = None
    try:
        lines = path.read_text(encoding="utf-8", errors="replace").splitlines()
    except OSError:
        return []

    for raw in lines:
        line = raw.strip()
        if line.startswith("- enabled:"):
            if current and current.get("path"):
                scenes.append(current)
            enabled_value = line.split(":", 1)[1].strip()
            current = {
                "enabled": enabled_value in {"1", "true", "True"},
                "path": "",
                "guid": "",
            }
            continue
        if current is None:
            continue
        if line.startswith("path:"):
            current["path"] = line.split(":", 1)[1].strip()
        elif line.startswith("guid:"):
            current["guid"] = line.split(":", 1)[1].strip()

    if current and current.get("path"):
        scenes.append(current)
    return scenes


def decision_ref(decision: Optional[Decision]) -> dict[str, object]:
    if decision is None:
        return {
            "status": "missing_from_scan",
            "file_kind": "missing_from_scan",
            "size_bytes": None,
            "reason": "Path was referenced by project settings but was not encountered during the scan.",
            "matched_rule": "",
        }
    return {
        "status": decision.status,
        "file_kind": decision.file_kind,
        "size_bytes": decision.size_bytes,
        "reason": decision.reason,
        "matched_rule": decision.matched_rule,
    }


def build_review_flags(
    decisions: list[Decision], project_dir: Path, args: argparse.Namespace
) -> dict[str, object]:
    """Build a compact, human-readable risk report from the classification decisions.

    The report is not meant to be exhaustive. It surfaces places where a coding
    agent may miss important context despite the bundle being intentionally safe
    and compact.
    """
    by_rel = {d.rel_path: d for d in decisions}

    editor_build_scenes: list[dict[str, object]] = []
    for scene in extract_editor_build_scenes(project_dir):
        rel = str(scene.get("path") or "").replace("\\", "/")
        decision = by_rel.get(rel)
        ref = decision_ref(decision)
        editor_build_scenes.append(
            {
                "path": rel,
                "enabled": bool(scene.get("enabled")),
                "guid": str(scene.get("guid") or ""),
                **ref,
            }
        )

    omitted_build_scenes = [
        s for s in editor_build_scenes if s.get("status") != "included"
    ]

    source_exts = SOURCE_EXTENSIONS
    skipped_source_like = [
        d
        for d in decisions
        if d.status == "skipped"
        and d.entry_type == "file"
        and suffix_lower(d.rel_path) in source_exts
    ]
    skipped_nonvendor_source_like = [
        d
        for d in skipped_source_like
        if d.file_kind not in {"vendor_or_sample_asset", "vendor_or_sample_meta"}
    ]

    skipped_editor_assets = [
        d
        for d in decisions
        if d.status == "skipped"
        and d.entry_type == "file"
        and d.file_kind == "unity_editor_authored_asset"
    ]
    skipped_editor_assets_sorted = sorted(
        skipped_editor_assets, key=lambda d: d.size_bytes or 0, reverse=True
    )

    included_large_text = sorted(
        [
            d
            for d in decisions
            if d.status == "included"
            and d.entry_type == "file"
            and (d.size_bytes or 0) >= int(args.review_large_included_kb * 1024)
        ],
        key=lambda d: d.size_bytes or 0,
        reverse=True,
    )

    def compact_decisions(items: list[Decision], limit: int) -> list[dict[str, object]]:
        return [
            {
                "path": d.rel_path,
                "size_bytes": d.size_bytes,
                "file_kind": d.file_kind,
                "reason": d.reason,
                "matched_rule": d.matched_rule,
            }
            for d in items[:limit]
        ]

    include_pattern_checks: list[dict[str, object]] = []
    include_pattern_problems: list[dict[str, object]] = []
    for pattern in getattr(args, "_effective_include_patterns", []):
        matched = [
            d
            for d in decisions
            if d.entry_type == "file" and pattern_matches(pattern, d.rel_path)
        ]
        included_matches = [d for d in matched if d.status == "included"]
        skipped_matches = [d for d in matched if d.status == "skipped"]
        check = {
            "pattern": pattern,
            "matched_files": len(matched),
            "included_files": len(included_matches),
            "skipped_files": len(skipped_matches),
            "skipped_top": compact_decisions(
                sorted(skipped_matches, key=lambda d: d.size_bytes or 0, reverse=True),
                min(args.review_list_limit, 5),
            ),
        }
        include_pattern_checks.append(check)
        if not matched or not included_matches or skipped_matches:
            include_pattern_problems.append(check)

    return {
        "editor_build_scenes": editor_build_scenes,
        "omitted_build_scenes_count": len(omitted_build_scenes),
        "omitted_build_scenes": omitted_build_scenes,
        "skipped_source_like_files_count": len(skipped_source_like),
        "skipped_vendor_source_like_files_count": len(skipped_source_like)
        - len(skipped_nonvendor_source_like),
        "skipped_nonvendor_source_like_files_count": len(skipped_nonvendor_source_like),
        "skipped_nonvendor_source_like_files_top": compact_decisions(
            sorted(
                skipped_nonvendor_source_like,
                key=lambda d: d.size_bytes or 0,
                reverse=True,
            ),
            args.review_list_limit,
        ),
        "skipped_editor_authored_context_count": len(skipped_editor_assets),
        "skipped_editor_authored_context_bytes": sum(
            d.size_bytes or 0 for d in skipped_editor_assets
        ),
        "skipped_editor_authored_context_top": compact_decisions(
            skipped_editor_assets_sorted, args.review_list_limit
        ),
        "large_included_files_count": len(included_large_text),
        "large_included_files_top": compact_decisions(
            included_large_text, args.review_list_limit
        ),
        "explicit_include_patterns_count": len(include_pattern_checks),
        "explicit_include_pattern_problems_count": len(include_pattern_problems),
        "explicit_include_pattern_problems": include_pattern_problems[
            : args.review_list_limit
        ],
    }


def make_review_report(
    project_name: str, summary: dict, review_flags: dict[str, object]
) -> str:
    def fmt_size(value: object) -> str:
        return format_bytes(value if isinstance(value, int) else None)

    omitted_scenes = review_flags.get("omitted_build_scenes", []) or []
    editor_context_top = (
        review_flags.get("skipped_editor_authored_context_top", []) or []
    )
    skipped_source_top = (
        review_flags.get("skipped_nonvendor_source_like_files_top", []) or []
    )
    large_included_top = review_flags.get("large_included_files_top", []) or []

    lines: list[str] = []
    lines.append(f"# Bundle Review Flags: {project_name}")
    lines.append("")
    lines.append(
        "This file highlights review items that may matter to a coding agent. These are not necessarily mistakes; the bundle is intentionally conservative."
    )
    lines.append("")

    lines.append("## Build scenes")
    if omitted_scenes:
        lines.append(
            f"{len(omitted_scenes)} scene(s) referenced by `ProjectSettings/EditorBuildSettings.asset` were not included in the bundle:"  # type: ignore
        )
        lines.append("")
        for item in omitted_scenes:  # type: ignore
            enabled = "enabled" if item.get("enabled") else "disabled"
            lines.append(
                f"- `{item.get('path')}` ({enabled}, {fmt_size(item.get('size_bytes'))}) — {item.get('reason')}"
            )
        lines.append("")
        lines.append(
            "For tasks involving scene wiring, UI bindings, serialized object references, or build-scene behavior, consider a second context bundle with `--include-editor-assets` and a higher `--max-file-mb`, or target only the needed scene with `--include-pattern`."
        )
    else:
        lines.append(
            "All scenes referenced by `ProjectSettings/EditorBuildSettings.asset` were included, or no build scenes were found."
        )
    lines.append("")

    lines.append("## Explicit include patterns")
    include_problem_count = review_flags.get(
        "explicit_include_pattern_problems_count", 0
    )
    include_patterns_count = review_flags.get("explicit_include_patterns_count", 0)
    include_problems = review_flags.get("explicit_include_pattern_problems", []) or []
    if include_problem_count:
        lines.append(
            f"{include_problem_count} explicit include pattern(s) need review because they matched no files, included no files, or had some matching files skipped:"
        )
        lines.append("")
        for item in include_problems:  # type: ignore
            lines.append(
                f"- `{item.get('pattern')}` — matched {item.get('matched_files')} file(s), "
                f"included {item.get('included_files')}, skipped {item.get('skipped_files')}"
            )
            skipped_top = item.get("skipped_top", []) or []
            for skipped in skipped_top:
                lines.append(
                    f"  - skipped `{skipped.get('path')}` ({fmt_size(skipped.get('size_bytes'))}) — {skipped.get('reason')}"
                )
        lines.append("")
        lines.append(
            "Check for typos, size-cap issues, secret/sensitive exclusions, or files that are binary/non-text."
        )
    elif include_patterns_count:
        lines.append(
            "All explicit/project-local include patterns matched at least one included file."
        )
    else:
        lines.append(
            "No explicit or project-local include patterns were used for this bundle."
        )
    lines.append("")

    lines.append("## Skipped source-like files")
    nonvendor_count = int(
        review_flags.get("skipped_nonvendor_source_like_files_count", 0) or 0  # type: ignore
    )
    vendor_count = int(
        review_flags.get("skipped_vendor_source_like_files_count", 0) or 0  # type: ignore
    )
    if nonvendor_count:
        lines.append(
            f"{nonvendor_count} non-vendor source-like file(s) were skipped. Review these before sharing the bundle:"
        )
        lines.append("")
        for item in skipped_source_top:  # type: ignore
            lines.append(
                f"- `{item.get('path')}` ({fmt_size(item.get('size_bytes'))}) — {item.get('reason')}"
            )
    else:
        lines.append("No non-vendor source-like files were skipped.")
    if vendor_count:
        lines.append(
            f"{vendor_count} source-like file(s) were skipped because they matched vendor/sample patterns. This is usually desirable for compact coding-agent bundles; use `--include-vendor-assets` or targeted `--include-pattern` only when a task needs dependency source."
        )
    lines.append("")

    lines.append("## Skipped Unity editor-authored context")
    editor_count = int(
        review_flags.get("skipped_e`ditor_authored_context_count", 0) or 0  # type: ignore
    )
    editor_bytes = int(
        review_flags.get("skipped_editor_authored_context_bytes", 0) or 0  # type: ignore
    )
    if editor_count:
        lines.append(
            f"{editor_count} first-party Unity editor-authored file(s) were skipped, totaling {format_bytes(editor_bytes)}. They are usually not edited by hand, but can be useful for understanding serialized bindings."
        )
        lines.append("")
        lines.append("Largest skipped editor-authored context files:")
        lines.append("")
        for item in editor_context_top:  # type: ignore
            lines.append(
                f"- `{item.get('path')}` ({fmt_size(item.get('size_bytes'))}) — {item.get('reason')}"
            )
    else:
        lines.append("No first-party Unity editor-authored context files were skipped.")
    lines.append("")

    lines.append("## Large included files")
    if large_included_top:
        threshold_kb = summary.get("review_large_included_kb") or "configured"
        lines.append(
            f"Included files at or above the large-file review threshold ({threshold_kb} KiB):"
        )
        lines.append("")
        for item in large_included_top:  # type: ignore
            lines.append(
                f"- `{item.get('path')}` ({fmt_size(item.get('size_bytes'))}) — {item.get('file_kind')}"
            )
    else:
        lines.append(
            "No included source/config files exceeded the large-file review threshold."
        )
    lines.append("")

    lines.append("## Suggested follow-up commands")
    lines.append("")
    project = summary.get("project_name") or project_name
    lines.append("For the normal code-only bundle:")
    lines.append("")
    lines.append(
        f"```bash\npython tools/build_unity_bundle.py {json.dumps(project)}\n```"
    )
    lines.append("")
    lines.append(
        "For a scene/prefab context pass while still excluding vendor/sample roots:"
    )
    lines.append("")
    lines.append(
        f"```bash\npython tools/build_unity_bundle.py {json.dumps(project)} --include-editor-assets --max-file-mb 2\n```"
    )
    lines.append("")
    if omitted_scenes:
        largest_scene = max(
            omitted_scenes, key=lambda item: int(item.get("size_bytes") or 0)  # type: ignore
        )  # type: ignore
        suggested_mib = max(
            2, int((int(largest_scene.get("size_bytes") or 0) / (1024 * 1024)) + 1)
        )
        scene_path = largest_scene.get("path") or "Assets/Scenes/Main.unity"
        lines.append("For only the largest omitted build scene:")
        lines.append("")
        lines.append(
            f"```bash\npython tools/build_unity_bundle.py {json.dumps(project)} --include-pattern {json.dumps(scene_path)} --max-file-mb {suggested_mib}\n```"
        )
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def summarize(
    decisions: list[Decision],
    args: argparse.Namespace,
    project_dir: Path,
    output_zip: Path,
    zip_root: str,
) -> dict:
    included = [
        d for d in decisions if d.status == "included" and d.entry_type == "file"
    ]
    skipped = [d for d in decisions if d.status == "skipped" and d.entry_type == "file"]
    errors = [d for d in decisions if d.status == "error"]

    by_kind: dict[str, int] = {}
    by_exclusion: dict[str, int] = {}
    included_roots: dict[str, dict[str, int]] = {}
    for d in decisions:
        by_kind[d.file_kind] = by_kind.get(d.file_kind, 0) + 1
        if d.status == "skipped":
            by_exclusion[d.file_kind] = by_exclusion.get(d.file_kind, 0) + 1
        if d.status == "included" and d.entry_type == "file":
            key = summary_root_key(d.rel_path)
            rec = included_roots.setdefault(key, {"files": 0, "bytes": 0})
            rec["files"] += 1
            rec["bytes"] += d.size_bytes or 0

    included_top_roots = [
        {"path": path, "files": rec["files"], "bytes": rec["bytes"]}
        for path, rec in sorted(
            included_roots.items(), key=lambda item: item[1]["bytes"], reverse=True
        )
    ]

    return {
        "tool_version": TOOL_VERSION,
        "generated_at_utc": utc_now_iso(),
        "project_name": project_dir.name,
        "project_dir": str(project_dir),
        "zip_root": zip_root,
        "output_zip": str(output_zip),
        "command": " ".join(sys.argv),
        "max_file_mib": args.max_file_mb,
        "include_editor_assets": args.include_editor_assets,
        "include_unknown_text": args.include_unknown_text,
        "include_all_meta": args.include_all_meta,
        "exclude_vendor_assets": not args.include_vendor_assets,
        "no_default_vendor_patterns": args.no_default_vendor_patterns,
        "vendor_pattern_count": len(getattr(args, "_vendor_patterns", [])),
        "full_json_manifest": args.full_json_manifest,
        "allow_binary": args.allow_binary,
        "allow_sensitive": args.allow_sensitive,
        "allow_potential_secrets": args.allow_potential_secrets,
        "prune_generated_dirs": args.prune_generated_dirs,
        "project_include_pattern_files": getattr(
            args, "_project_include_pattern_files", []
        ),
        "project_exclude_pattern_files": getattr(
            args, "_project_exclude_pattern_files", []
        ),
        "effective_include_pattern_count": getattr(
            args, "_effective_include_pattern_count", 0
        ),
        "effective_exclude_pattern_count": getattr(
            args, "_effective_exclude_pattern_count", 0
        ),
        "effective_include_patterns": getattr(args, "_effective_include_patterns", []),
        "effective_exclude_patterns": getattr(args, "_effective_exclude_patterns", []),
        "scanned_entries": len(decisions),
        "scanned_file_entries": sum(1 for d in decisions if d.entry_type == "file"),
        "included_files": len(included),
        "skipped_files": len(skipped),
        "error_entries": len(errors),
        "included_bytes": sum(d.size_bytes or 0 for d in included),
        "included_top_roots_by_bytes": included_top_roots,
        "review_large_included_kb": args.review_large_included_kb,
        "counts_by_file_kind": dict(sorted(by_kind.items())),
        "skipped_counts_by_file_kind": dict(sorted(by_exclusion.items())),
    }


def build_bundle(args: argparse.Namespace) -> tuple[Path, dict]:
    script_path = Path(__file__).resolve()
    repo_root = resolve_repo_root(script_path, args.repo_root)
    project_dir = resolve_project_dir(repo_root, args.project)
    validate_unity_project(project_dir, args.allow_non_unity)

    out_dir = args.out_dir if args.out_dir else repo_root / "artifacts" / "bundles"
    out_dir = out_dir.resolve()
    timestamp = _dt.datetime.now().strftime("%Y%m%d_%H%M%S")
    safe_project_name = (
        re.sub(r"[^A-Za-z0-9_.-]+", "_", project_dir.name).strip("_") or "unity_project"
    )
    zip_name = args.zip_name or f"{safe_project_name}_bundle_{timestamp}.zip"
    if not zip_name.lower().endswith(".zip"):
        zip_name += ".zip"
    output_zip = (out_dir / zip_name).resolve()

    if output_zip.exists() and not args.overwrite and not args.dry_run:
        raise SystemExit(
            f"Output zip already exists: {output_zip}. Use --overwrite or --zip-name."
        )

    project_include_patterns: list[str] = []
    project_exclude_patterns: list[str] = []
    project_include_sources: list[str] = []
    project_exclude_sources: list[str] = []
    if not args.ignore_project_pattern_files:
        project_include_patterns, project_include_sources = read_project_pattern_files(
            project_dir, PROJECT_INCLUDE_PATTERN_FILENAMES
        )
        project_exclude_patterns, project_exclude_sources = read_project_pattern_files(
            project_dir, PROJECT_EXCLUDE_PATTERN_FILENAMES
        )

    include_patterns = project_include_patterns + [
        normalize_pattern(p) for p in args.include_pattern
    ]
    include_patterns += read_patterns_file(args.include_from)
    exclude_patterns = project_exclude_patterns + [
        normalize_pattern(p) for p in args.exclude_pattern
    ]
    exclude_patterns += read_patterns_file(args.exclude_from)
    vendor_patterns = (
        []
        if args.no_default_vendor_patterns
        else [normalize_pattern(p) for p in DEFAULT_VENDOR_OR_SAMPLE_ASSET_PATTERNS]
    )
    vendor_patterns += [normalize_pattern(p) for p in args.vendor_pattern]
    vendor_patterns += read_patterns_file(args.vendor_from)

    args._project_include_pattern_files = project_include_sources
    args._project_exclude_pattern_files = project_exclude_sources
    args._effective_include_patterns = include_patterns
    args._effective_exclude_patterns = exclude_patterns
    args._effective_include_pattern_count = len(include_patterns)
    args._effective_exclude_pattern_count = len(exclude_patterns)
    args._vendor_patterns = vendor_patterns

    ctx = RuleContext(
        project_dir=project_dir,
        output_zip=output_zip,
        max_file_bytes=int(args.max_file_mb * 1024 * 1024),
        include_editor_assets=args.include_editor_assets,
        include_unknown_text=args.include_unknown_text,
        include_all_meta=args.include_all_meta,
        allow_binary=args.allow_binary,
        allow_sensitive=args.allow_sensitive,
        allow_potential_secrets=args.allow_potential_secrets,
        exclude_vendor_assets=not args.include_vendor_assets,
        hash_skipped=args.hash_skipped,
        include_patterns=include_patterns,
        exclude_patterns=exclude_patterns,
        vendor_patterns=vendor_patterns,
        sensitive_patterns=DEFAULT_SENSITIVE_PATTERNS,
    )

    zip_root = safe_project_name

    entries = list(
        iter_project_entries(
            project_dir, prune_generated_dirs=args.prune_generated_dirs
        )
    )
    non_meta_entries = [
        e
        for e in entries
        if not e.rel_path.endswith(".meta") and e.entry_type == "file"
    ]
    meta_entries = [
        e for e in entries if e.rel_path.endswith(".meta") and e.entry_type == "file"
    ]
    directory_entries = [e for e in entries if e.entry_type == "directory"]

    decisions_by_rel: dict[str, Decision] = {}
    decisions: list[Decision] = []

    for entry in directory_entries:
        gen_dir, gen_reason = generated_directory_reason(entry.rel_path.rstrip("/"))
        if gen_dir:
            d = skip_decision(
                entry,
                f"Pruned directory: {gen_reason} Contents were not scanned. Use --scan-generated-dirs only when you need an exhaustive generated/cache/container listing.",
                "pruned_directory",
                gen_dir,
            )
        else:
            d = skip_decision(
                entry, "Directory marker; no file content bundled.", "directory"
            )
        decisions_by_rel[entry.rel_path] = d
        decisions.append(d)

    for entry in non_meta_entries:
        try:
            decision = classify_non_meta(entry, ctx, zip_root)
        except (
            Exception
        ) as exc:  # keep manifest robust even when a file is odd/inaccessible
            decision = error_decision(entry, f"Error while classifying: {exc}")
        decisions_by_rel[entry.rel_path] = decision
        decisions.append(decision)

    included_files = {
        d.rel_path
        for d in decisions
        if d.status == "included" and d.entry_type == "file"
    }
    included_dirs = build_included_dir_set(included_files)

    for entry in meta_entries:
        try:
            decision = classify_meta(
                entry, ctx, zip_root, included_files, included_dirs
            )
        except Exception as exc:
            decision = error_decision(
                entry, f"Error while classifying .meta file: {exc}"
            )
        decisions_by_rel[entry.rel_path] = decision
        decisions.append(decision)

    # Stable order in manifests and zip.
    decisions.sort(key=lambda d: (d.rel_path.rstrip("/").lower(), d.entry_type))

    # Hash included files, and optionally skipped files. Hashing skipped files is off by default
    # so large/sensitive files are not read unnecessarily.
    for d in decisions:
        if d.entry_type != "file":
            continue
        if d.status == "included" or (d.status == "skipped" and ctx.hash_skipped):
            try:
                d.sha256 = sha256_file(project_dir / d.rel_path)
            except Exception as exc:
                if d.status == "included":
                    d.status = "error"
                    d.reason = f"Error while hashing included file: {exc}"
                    d.exclusion_reason = d.reason
                    d.file_kind = "error"
                    d.zip_path = ""

    summary = summarize(decisions, args, project_dir, output_zip, zip_root)
    review_flags = build_review_flags(decisions, project_dir, args)
    summary["review_flags"] = review_flags

    # Manifest files are generated outside first, then inserted into the bundle.
    temp_dir = out_dir / f".{safe_project_name}_bundle_tmp_{timestamp}"
    manifest_csv = temp_dir / "BUNDLE_MANIFEST.csv"
    summary_json = temp_dir / "BUNDLE_SUMMARY.json"
    manifest_json = temp_dir / "BUNDLE_MANIFEST.json"
    agent_readme = temp_dir / "README_BUNDLE.md"
    review_report = temp_dir / "BUNDLE_REVIEW_FLAGS.md"

    if args.dry_run:
        print(json.dumps(summary, indent=2, ensure_ascii=False))
        return output_zip, summary

    out_dir.mkdir(parents=True, exist_ok=True)
    temp_dir.mkdir(parents=True, exist_ok=True)
    try:
        write_manifest_csv(manifest_csv, decisions)
        write_summary_json(summary_json, summary)
        if args.full_json_manifest:
            write_manifest_json(manifest_json, decisions, summary)
        agent_readme.write_text(
            make_agent_readme(project_dir.name, summary), encoding="utf-8"
        )
        review_report.write_text(
            make_review_report(project_dir.name, summary, review_flags),
            encoding="utf-8",
        )

        compression = zipfile.ZIP_DEFLATED
        with zipfile.ZipFile(
            output_zip, "w", compression=compression, compresslevel=9
        ) as zf:
            for d in decisions:
                if d.status != "included" or d.entry_type != "file" or not d.zip_path:
                    continue
                src = project_dir / d.rel_path
                # Skip files that became problematic after classification; update console warning but
                # leave the manifest decision as the classification-time result.
                try:
                    zf.write(src, d.zip_path)
                except OSError as exc:
                    print(
                        f"Warning: could not add {d.rel_path}: {exc}", file=sys.stderr
                    )

            zf.write(agent_readme, f"{zip_root}/README_BUNDLE.md")
            zf.write(manifest_csv, f"{zip_root}/BUNDLE_MANIFEST.csv")
            zf.write(summary_json, f"{zip_root}/BUNDLE_SUMMARY.json")
            zf.write(review_report, f"{zip_root}/BUNDLE_REVIEW_FLAGS.md")
            if args.full_json_manifest:
                zf.write(manifest_json, f"{zip_root}/BUNDLE_MANIFEST.json")

        return output_zip, summary
    finally:
        # Best-effort cleanup of temporary generated manifest files.
        try:
            for p in (
                manifest_csv,
                summary_json,
                manifest_json,
                agent_readme,
                review_report,
            ):
                if p.exists():
                    p.unlink()
            temp_dir.rmdir()
        except OSError:
            pass


def positive_float(value: str) -> float:
    try:
        result = float(value)
    except ValueError as exc:
        raise argparse.ArgumentTypeError("must be a number") from exc
    if result <= 0:
        raise argparse.ArgumentTypeError("must be greater than zero")
    return result


