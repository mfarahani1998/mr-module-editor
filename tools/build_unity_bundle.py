def include_decision(
    entry: Entry, reason: str, file_kind: str, zip_root: str, matched_rule: str = ""
) -> Decision:
    zip_path = f"{zip_root}/{entry.rel_path}" if zip_root else entry.rel_path
    return Decision(
        rel_path=entry.rel_path,
        entry_type=entry.entry_type,
        status="included",
        reason=reason,
        exclusion_reason="",
        file_kind=file_kind,
        size_bytes=entry.size_bytes,
        mtime_utc=entry.mtime_utc,
        zip_path=zip_path,
        matched_rule=matched_rule,
    )


