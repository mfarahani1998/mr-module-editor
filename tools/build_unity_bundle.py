def main(argv: Optional[list[str]] = None) -> int:
    args = parse_args(argv)
    try:
        output_zip, summary = build_bundle(args)
    except SystemExit:
        raise
    except Exception as exc:
        print(f"Error: {exc}", file=sys.stderr)
        return 2

    if args.dry_run:
        return 0

    print(f"Created: {output_zip}")
    print(
        f"Included {summary['included_files']} files "
        f"({summary['included_bytes']} bytes); skipped {summary['skipped_files']} files; "
        f"errors {summary['error_entries']}."
    )
    if args.top_included_roots > 0:
        roots = summary.get("included_top_roots_by_bytes", [])[
            : args.top_included_roots
        ]
        if roots:
            print("Largest included roots by uncompressed bytes:")
            for root in roots:
                print(
                    f"  {root['bytes']:>10} bytes  {root['files']:>5} files  {root['path']}"
                )
    review_flags = summary.get("review_flags", {})
    omitted_scene_count = (
        review_flags.get("omitted_build_scenes_count", 0)
        if isinstance(review_flags, dict)
        else 0
    )
    nonvendor_source_count = (
        review_flags.get("skipped_nonvendor_source_like_files_count", 0)
        if isinstance(review_flags, dict)
        else 0
    )
    include_problem_count = (
        review_flags.get("explicit_include_pattern_problems_count", 0)
        if isinstance(review_flags, dict)
        else 0
    )
    if omitted_scene_count or nonvendor_source_count or include_problem_count:
        print(
            f"Review flags: omitted build scenes={omitted_scene_count}; "
            f"skipped non-vendor source-like files={nonvendor_source_count}; "
            f"explicit include pattern issues={include_problem_count}. "
            "See BUNDLE_REVIEW_FLAGS.md inside the zip."
        )
    print(
        "Open BUNDLE_MANIFEST.csv inside the zip for every scanned file/directory and its include/exclude reason."
    )
    if not args.full_json_manifest:
        print(
            "Tip: use --full-json-manifest only when another tool needs a row-by-row JSON manifest."
        )
    return 0


